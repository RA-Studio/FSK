<div>
	 О поступлениях оплат и выдаче актов сверки; <br>
	 По вопросам, касающимся документооборота к Вашему договору; <br>
	 По вопросам направленных в Ваш адрес ответов и уведомлений; <br>
	 О начале осмотров и по вопросам передачи квартир; <br>
	 О постановке объекта на кадастровый учет; <br>
	 О порядке подачи заявки в управляющую компанию.<br>
 <br>
</div>
