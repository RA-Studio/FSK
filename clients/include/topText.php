<h3>Прием и регистрация входящих обращений.</h3>
 Обращаем Ваше внимание, что в случае возникновения вопросов, требующих письменного ответа, сохраняется необходимость в официальной переписке. Обращение оформляется на имя Генерального директора ООО "ФСК "Лидер Северо-Запад" на бумажном носителе с указанием даты, с Вашей подписью и расшифровкой.
<p>
	 Обращение может быть направлено почтой РФ, оформлено в офисе Застройщика (ежедневно с 9-21 часа) или направлено в виде скан копии на адрес эл.почты&nbsp;<b><a href="mailto:oks@fsknw.ru">oks@fsknw.ru</a></b>. Для вашего удобства, ниже размещен шаблон заявления. Вы можете воспользоваться им для составления обращения к Застройщику.
</p>
